        $answer->uid= Auth::user()->id;
        $answer->answer=$request->answer;
        $answer->question_id = $request->question_id;
        if($answer->save())
        {
            return back()->with('success', 'your answer successfully posted');
        }else{
            return back()->with('error', 'Could not be posted please try again');
